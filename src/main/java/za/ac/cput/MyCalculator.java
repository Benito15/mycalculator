package za.ac.cput;

public class MyCalculator
{
    //Addition
    public int add(int a, int b)
    {
        return a + b;
    }

    //Multiplication
    public int multi(int a, int b)
    {
        return a * b;
    }

    //Subtraction
    public int sub(int a , int b)
    {
        return a - b;
    }

    //Division
    public int div(int a, int b)
    {
        return a / b;
    }
}
